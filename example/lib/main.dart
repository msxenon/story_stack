class UserModel {
  UserModel(this.stories, this.userName, this.imageUrl);

  final List<StoryModel> stories;
  final String userName;
  final String imageUrl;
}

class StoryModel {
  StoryModel(this.imageUrl);

  final String imageUrl;
}

class MyApp extends StatelessWidget {
  const MyApp({super.key});

  // This widget is the root of your application.
  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      title: 'Flutter Demo',
      debugShowCheckedModeBanner: false,
      theme: ThemeData(
        primarySwatch: Colors.blue,
        visualDensity: VisualDensity.adaptivePlatformDensity,
      ),
      home: const MyHomePage(),
    );
  }
}

class MyHomePage extends StatelessWidget {
  const MyHomePage({super.key});

  void _openStories(
    BuildContext context,
    int initialPage, {
    int initialStoryIndex = 0,
  }) {
    Navigator.push(
      context,
      // A regular MaterialPageRoute is opaque: once its push transition
      // finishes, Flutter stops painting the route underneath at all —
      // so when StoryPageView's pull-to-dismiss shrinks/fades the page,
      // there's nothing behind it but the Scaffold's plain white
      // background. PageRouteBuilder with opaque: false (and no barrier
      // scrim) keeps this screen alive and visible underneath instead.
      PageRouteBuilder<void>(
        opaque: false,
        barrierColor: Colors.transparent,
        transitionDuration: const Duration(milliseconds: 200),
        reverseTransitionDuration: const Duration(milliseconds: 200),
        pageBuilder: (context, animation, secondaryAnimation) {
          return StoryPage(
            initialPage: initialPage,
            initialStoryIndex: initialStoryIndex,
          );
        },
        transitionsBuilder: (context, animation, secondaryAnimation, child) {
          return FadeTransition(opacity: animation, child: child);
        },
      ),
    );
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      body: SafeArea(
        child: Column(
          children: [
            StoryCirclesList(
              users: [
                for (final user in sampleUsers)
                  StoryCircleUser(
                    id: user.userName,

                    imageProvider: NetworkImage(user.imageUrl),
                    storyCount: user.stories.length,
                  ),
              ],
              // None of these have been seen, so always open at the first
              // story (index 0).
              onTapUser: (index) => _openStories(context, index),
            ),
            Text(
              'Some unseen stories',
              style: Theme.of(context).textTheme.headlineSmall,
              textAlign: TextAlign.center,
            ),
            StoryCirclesList(
              users: [
                for (final user in sampleUsers)
                  StoryCircleUser(
                    id: user.userName,

                    imageProvider: NetworkImage(user.imageUrl),
                    storyCount: user.stories.length,
                    seenCount: user.stories.length - 1,
                  ),
              ],
              // All but the last story have been seen, so tapping a circle
              // should jump straight to that first unseen story instead of
              // making the user re-watch ones they've already seen.
              onTapUser: (index) => _openStories(
                context,
                index,
                initialStoryIndex: sampleUsers[index].stories.length - 1,
              ),
            ),
            Text(
              'Stories with names',
              style: Theme.of(context).textTheme.headlineSmall,
              textAlign: TextAlign.center,
            ),
            StoryCirclesList(
              users: [
                for (final user in sampleUsers)
                  StoryCircleUser(
                    id: user.userName,
                    name: Text(
                      user.userName,
                      maxLines: 1,
                      overflow: TextOverflow.ellipsis,
                      textAlign: TextAlign.center,
                      style: const TextStyle(fontSize: 12),
                    ),
                    imageProvider: NetworkImage(user.imageUrl),
                    storyCount: user.stories.length,
                  ),
              ],
              onTapUser: (index) => _openStories(context, index),
            ),

            Expanded(
              child: Center(
                child: ElevatedButton(
                  child: const Text('show stories'),
                  onPressed: () => _openStories(context, 0),
                ),
              ),
            ),
          ],
        ),
      ),
    );
  }
}

